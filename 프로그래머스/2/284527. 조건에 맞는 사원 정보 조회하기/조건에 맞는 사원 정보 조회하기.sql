SELECT SUM(B.SCORE) AS SCORE, A.EMP_NO, A.EMP_NAME, A.POSITION, A.EMAIL
FROM HR_EMPLOYEES A
JOIN HR_GRADE B
ON A.EMP_NO = B.EMP_NO
GROUP BY A.EMP_NO, A.EMP_NAME, A.POSITION, A.EMAIL
HAVING SUM(B.SCORE) = (SELECT MAX(TOTAL_SCORE)
                      FROM ( SELECT SUM(SCORE) AS TOTAL_SCORE FROM HR_GRADE GROUP BY EMP_NO)MAX_SCORE)
ORDER BY 1 DESC;

